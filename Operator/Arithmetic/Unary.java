package Operator.Arithmetic;

public class Unary {
    public static void main(String[] args) {
        int a = 39;
       //pre decrement --a;
        System.out.println(a);
        a++; // post increment
        ++a;// pre increment
        System.out.println(a);
        a--;// post decrement
        System.out.println(a);
    }
}
