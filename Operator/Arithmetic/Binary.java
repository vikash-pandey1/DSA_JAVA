package Operator.Arithmetic;

public class Binary {
    public static void main(String[] args) {
        int a = 399;
        int b = 39;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
