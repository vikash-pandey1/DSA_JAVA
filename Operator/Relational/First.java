package Operator.Relational;

public class First {
    public static void main(String[] args) {
        // equal operator ==
        // not equal operator !=
        // greater than >
        // less than <
        // greater than equal to >=
        // less than equal to <=
        int a = 39;
        int b = 30;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
}
